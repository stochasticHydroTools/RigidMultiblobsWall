import numpy as np
from scipy.interpolate import PchipInterpolator

def configure_grid_and_kernels_xy(Lx, Ly, pRs, kernTypes, optInd, has_torque=False, ref=False):
  """
  Function to set up the xy grid given x-y box length L, particle radius and kernel type.
  The grid size can be chosen as fft friendly - with factorizations like 2^p * 3^q * 5^r * 7^s
    
 
  Input: Lx, Ly  - length in x,y
         pRs - list of particle radii
         kernTypes - array of types of kernel for each particle
                  - 0 for ES 6 pt (both monopole and dipole if has_torque = true) 
                  - 1 for ES 5 pt (both monopole and dipole if has_torque = true)
                  - 2 for ES 4 pt (only monopole, dipole not supported) 
                  - 3 for ES 6 pt monopole, ES 5 pt dipole
                  - 4 for ES 6 pt monopole, ES 4 pt dipole
                  - 5 for ES 5 pt monopole, ES 4 pt dipole 
         has_torque - if True, problem involves torque (default is False)
         ref - if True, then grid will be a reference (doubled) grid for the given radii
         optInd - selects kernel at index (see stdout)
                - using optInd=-1 will select the optimal/adjusted grid
                - optInd=0,1,.. will select the corresponding row of stdout
  
  Output:  Lx, Ly - lengths in x, y (possibly corrected)
           hx, hy - grid spacing in x,y
           Nx, Ny - number of points in x, y, 
           wm, wd    - number of points underneath monopole/dipole kernels of each particle
                     - if has_torque = false, wd = 0
                     - if has_torque = true, wm and wd are filled
           cbeta - dimensionless radius for each kernelType as function of beta
           betafP, betadP - ES kernel beta for monopole and dipole (betadP = 0 if has_torque=False)
  Stdout: 
          - The optimal *adjusted* grid is displayed (optInd = -1)
          - Several candidate fft-friendly grids are displayed (optInd = 0,1,..) 
          - The final kernel settings for each particle size are displayed
  """
  nP = pRs.size; minPr = np.min(pRs);
  wm = np.zeros(nP, dtype = np.ushort)
  cbeta_m = np.zeros(nP); cbeta_d = np.zeros(nP)
  betafP = np.zeros(nP)
  h = np.zeros(nP);
  # polynomial handles given radii polyfit coefs as a function of beta*w and inverse
  radPolym = np.poly1d(cbetam)
  radPolyd = np.poly1d(cbetad)
  radPolym_inv = np.poly1d(cbetam_inv)
  radPolyd_inv = np.poly1d(cbetad_inv)
  pSet = set(pRs); pSet_test = set()
  # no torque
  if not has_torque:
    for iP in range(0, nP):
      # define kernels for each particle
      if kernTypes[iP] == 0: # ES 6 pt 
        wm[iP] = 6; betafP[iP] = 1.714;
        # optimal spacing but doesn't guarantee fft friendly grid
        hopt = minPr / 1.5539;  
      elif kernTypes[iP] == 1: # ES 5 pt
        wm[iP] = 5; betafP[iP] = 1.886;
        hopt = minPr / 1.3437; 
      elif kernTypes[iP] == 2: # ES 4 pt
        wm[iP] = 4; betafP[iP] = 1.785;
        hopt = minPr / 1.2047; 
      else:
        raise ValueError('translation-only kernel type not supported')
      # compute grid spacing for each particle
      h[iP] = pRs[iP] / (wm[iP] * radPolym(betafP[iP] * wm[iP]))
    # take the minimum grid spacing 
    h = np.min(h)
    # set torque vars to 0
    wd = [0]; betadP = [0];
    # real N given Lx and h 
    Nx_real = Lx / h;
    # search up to 10 larger fft friendly Ns (small prime factors - 2,3,5,7)
    Nx = fft_friendly_sizes(np.floor(Nx_real).astype(np.int),100,10)
    # candidate grid spacings
    hx = hy = Lx / Nx;

    # error fits as function of beta*w
    errw4_ax = np.linspace(4,12,1000)
    errw5_ax = np.linspace(5,15,1000)
    errw6_ax = np.linspace(6,18,1000)
    errPolyw4 = PchipInterpolator(errw4_ax, errmw4)
    errPolyw5 = PchipInterpolator(errw5_ax, errmw5)
    errPolyw6 = PchipInterpolator(errw6_ax, errmw6)
  
    betafP_test = np.zeros((nP, len(Nx)))
    betafP_opt = np.zeros(nP)
    err_test = np.zeros((nP, len(Nx)))
    err_opt = np.zeros(nP)
    # compute beta values and error for each particle and each candidate grid spacing
    for iP in range(0,nP):
      if kernTypes[iP] == 0:
        err = errmw6
        errPoly = errPolyw6
      elif kernTypes[iP] == 1:
        err = errmw5
        errPoly = errPolyw5
      elif kernTypes[iP] == 2:  
        err = errmw4
        errPoly = errPolyw4

      # compute beta and error for optimal grid spacing (not necessarily fft friendly)
      betafP_opt[iP] = radPolym_inv(pRs[iP] / (wm[iP] * hopt)) / wm[iP]
      # exit if one of the particles can't represented (out of range beta interp)
      if betafP_opt[iP] * wm[iP] < 4 or betafP_opt[iP] * wm[iP] > 18:
        raise ValueError('Could not find optimal (h,beta) in range for particle #%d' % (iP))
        err_opt[iP] = np.inf
      else:
        err_opt[iP] = errPoly(betafP_opt[iP] * wm[iP])
      for j in range(0,len(hx)):
        # get beta values for each candidate grid spacing
        betafP_test[iP,j] = radPolym_inv(pRs[iP] / (wm[iP] * hx[j])) / wm[iP]
        # ignore out of range inputs
        if betafP_test[iP,j] * wm[iP] < 4 or betafP_test[iP,j] * wm[iP] > 18:
          err_test[iP, j] = np.inf
        else:
          err_test[iP,j] = errPoly(betafP_test[iP,j] * wm[iP])

    err_test_ave = np.sum(err_test, axis=(0)) / nP
    err_opt_ave = np.sum(err_opt, axis=(0)) / nP
   
    #### print out and select grid options ############### 
    print('\n\tOptimal grid with adjusted extent (optInd=-1):\n')
    Nxopt = np.round(Lx / hopt).astype(np.int); Lxopt = hopt * Nxopt
    Nyopt = np.round(Ly / hopt).astype(np.int); Lyopt = hopt * Nyopt
    sout = [['L_x', 'L_y','N_x', 'N_y', 'h','%-err'],
            [Lxopt, Lyopt, Nxopt, Nyopt, hopt, err_opt_ave]]
    for row in sout:
      print("\t{:^16}\t{:^16}\t{:^8}\t{:^8}\t{:^16}\t{:^.5}".format(*row))

    print('\n\tCandidate grids with user-defined extent (optInd=0,1,..):\n')
    sout = [['N_x', 'h','%-err']]
    for j in range(0,len(hx)):
      if not np.isinf(err_test_ave[j]):
        sout.append([Nx[j], hx[j], err_test_ave[j]])
    for row in sout:
      print("\t{:^5}\t{:^16}\t{:^.5}".format(*row))

    mini = optInd
    ######################################################

    # user-selected grid
    if mini >= 0:    
      if np.isinf(err_test[:,mini]).any():    
        raise ValueError('Out of range interpolation')
      betafP = betafP_test[:,mini].copy()
      Nx = Nx[mini]; hx = hy = hx[mini]; 
      Ny = Ly / hy # TODO: Lx!=Ly
      if np.round(Ny) != Ny:
        print("Adjusting requested Ly for integer grid size...")
        Ny = np.ceil(Ny).astype(np.int); Ly = hy * Ny
    # optimal adjusted grid
    else:
      betafP = betafP_opt
      Lx = Lxopt; Ly = Lyopt; Nx = Nxopt; Ny = Nyopt
      hx = hopt; hy = hopt;

    # set dimensionless radii
    for iP in range(0,nP):
      cbeta_m[iP] = pRs[iP] / (wm[iP] * hx) 
    cbeta_d = 0
       
    # double the number of points under kernel for reference computation
    if ref:
      hx /= 2; Nx *= 2
      hy /= 2; Ny *= 2
 
    ################# printout kernel settings ############ 
    print('\n\tFinal kernel settings:\n')
    sout = [['R_h','w_m','beta_m']]
    for iP in range(0,nP):
      if pSet == pSet_test:
        break;
      if pRs[iP] not in pSet_test:
        pSet_test.add(pRs[iP])
        sout.append([pRs[iP], wm[iP], betafP[iP] * wm[iP]])
    for row in sout:
      print("\t{:^16}\t{:^5}\t{:^16}".format(*row))
    #########################################################
  
  # with torque
  else:
    wd = np.zeros(nP, dtype = np.ushort); betadP = np.zeros(nP)
    for iP in range(0,nP):
      # define kernels for each particle
      if kernTypes[iP] == 0: # ES 6 pt for both 
        wm[iP] = wd[iP] = 6; 
        betafP[iP] = 1.3267; betadP[iP] = 2.2160;
        # optimal spacing but doesn't guarantee fft friendly grid
        hopt = minPr / 1.7305; 
      elif kernTypes[iP] == 1: # ES 5 pt for both
        wm[iP] = wd[iP] = 5; 
        betafP[iP] = 1.3055; betadP[iP] = 2.2320;
        hopt = minPr / 1.5598; 
      elif kernTypes[iP] == 3: # 6 pt monopole, 5 pt dipole 
        wm[iP] = 6; wd[iP] = 5; 
        betafP[iP] = 1.3261; betadP[iP] = 1.7584;
        hopt = minPr / 1.7309; 
      elif kernTypes[iP] == 4: # 6 pt monopole, 4 pt dipole
        wm[iP] = 6; wd[iP] = 4;
        betafP[iP] = 1.5728; betadP[iP] = 1.5115;
        hopt = minPr / 1.6121; 
      elif kernTypes[iP] == 5: # 5 pt monopole, 4 pt dipole
        wm[iP] = 5; wd[iP] = 4;
        betafP[iP] = 1.3536; betadP[iP] = 1.7147;
        hopt = minPr / 1.5382; 
      else:
        raise ValueError('translation-rotation kernel type not supported')
      # compute grid spacing for each particle
      h[iP] = pRs[iP] / (wm[iP] * radPolym(betafP[iP] * wm[iP]))


    # take the minimum grid spacing to ensure all particles are well resolved
    h = np.min(h)
    # real N given Lx and h 
    Nx_real = Lx / h;
    # search up to 10 fft friendly Ns (small prime factors - 2,3,5,7)
    Nx = fft_friendly_sizes(np.floor(Nx_real).astype(np.int),20,10)
    # candidate grid spacings
    hx = hy = Lx / Nx;
    # error fits as function of beta*w
    errw4_ax = np.linspace(4,12,1000)
    errw5_ax = np.linspace(5,15,1000)
    errw6_ax = np.linspace(6,18,1000)
    errmPolyw4 = PchipInterpolator(errw4_ax, errmw4)
    errmPolyw5 = PchipInterpolator(errw5_ax, errmw5)
    errmPolyw6 = PchipInterpolator(errw6_ax, errmw6)
    errdPolyw4 = PchipInterpolator(errw4_ax, errdw4)
    errdPolyw5 = PchipInterpolator(errw5_ax, errdw5)
    errdPolyw6 = PchipInterpolator(errw6_ax, errdw6)
    betafP_test = np.zeros((nP, len(Nx)))
    betafP_opt = np.zeros(nP)
    betadP_test = np.zeros((nP, len(Nx)))
    betadP_opt= np.zeros(nP)
    errm_test = np.zeros((nP, len(Nx)))
    errm_opt = np.zeros(nP)
    errd_test = np.zeros((nP, len(Nx)))
    errd_opt = np.zeros(nP)
    # compute beta values and error for each particle and each candidate grid spacing
    for iP in range(0,nP):
      if kernTypes[iP] == 0:
        errm = errmw6
        errd = errdw6
        errmPoly = errmPolyw6
        errdPoly = errdPolyw6
      elif kernTypes[iP] == 1:
        errm = errmw5
        errd = errdw5
        errmPoly = errmPolyw5
        errdPoly = errdPolyw5
      elif kernTypes[iP] == 3:  
        errm = errmw6
        errd = errdw5
        errmPoly = errmPolyw6
        errdPoly = errdPolyw5
      elif kernTypes[iP] == 4:  
        errm = errmw6
        errd = errdw4
        errmPoly = errmPolyw6
        errdPoly = errdPolyw4
      elif kernTypes[iP] == 5:  
        errm = errmw5
        errd = errdw4
        errmPoly = errmPolyw5
        errdPoly = errdPolyw4

      # compute beta and error for optimal grid spacing (not necessarily fft friendly)
      betafP_opt[iP] = radPolym_inv(pRs[iP] / (wm[iP] * hopt)) / wm[iP]
      betadP_opt[iP] = radPolyd_inv(pRs[iP] / (wd[iP] * hopt)) / wd[iP]
      # exit if one of the particles can't represented (out of range beta interp)
      if (betafP_opt[iP] * wm[iP] < 4 or betafP_opt[iP] * wm[iP] > 18) or\
         (betadP_opt[iP] * wd[iP] < 4 or betadP_opt[iP] * wd[iP] > 18):
        raise ValueError('Could not find optimal (h,beta) in range for particle #%d' % (iP))
      else:
        errm_opt[iP] = errmPoly(betafP_opt[iP] * wm[iP])
        errd_opt[iP] = errdPoly(betadP_opt[iP] * wd[iP])
      for j in range(0,len(hx)):
        # get beta values for each candidate grid spacing
        betafP_test[iP,j] = radPolym_inv(pRs[iP] / (wm[iP] * hx[j])) / wm[iP]
        betadP_test[iP,j] = radPolyd_inv(pRs[iP] / (wd[iP] * hx[j])) / wd[iP]
        # ignore out of range inputs
        if betafP_test[iP,j] * wm[iP] < 4 or betafP_test[iP,j] * wm[iP] > 18:
          errm_test[iP, j] = np.inf
        else:
          errm_test[iP,j] = errmPoly(betafP_test[iP,j] * wm[iP])
        if betadP_test[iP,j] * wd[iP] < 4 or betadP_test[iP,j] * wd[iP] > 18:
          errd_test[iP, j] = np.inf
        else:
          errd_test[iP,j] = errdPoly(betadP_test[iP,j] * wd[iP])

    err_test_ave = np.sum(errm_test + errd_test, axis=(0)) / nP
    err_opt_ave = np.sum(errm_opt + errd_opt, axis=(0)) / nP

    #### print out and select grid options ############### 
    print('\n\tOptimal grid with adjusted extent (optInd=-1):\n')
    Nxopt = np.round(Lx / hopt).astype(np.int); Lxopt = hopt * Nxopt
    Nyopt = np.round(Ly / hopt).astype(np.int); Lyopt = hopt * Nyopt
    sout = [['L_x', 'L_y','N_x', 'N_y', 'h','%-err'],
            [Lxopt, Lyopt, Nxopt, Nyopt, hopt, err_opt_ave]]

    for row in sout:
      print("\t{:^16}\t{:^16}\t{:^8}\t{:^8}\t{:^16}\t{:^.5}".format(*row))
    
    print('\n\tCandidate grids with user-defined extent (optInd=0,1,..):\n')
    sout = [['N_x', 'h','%-err']]
    for j in range(0,len(hx)):
      if not np.isinf(err_test_ave[j]):
        sout.append([Nx[j], hx[j], err_test_ave[j]])
    for row in sout:
      print("\t{:^5}\t{:^16}\t{:^.5}".format(*row))

    mini = optInd

    #####################################################

    if mini >= 0:
      if np.isinf(errm_test[:,mini]).any():    
        raise ValueError('Out of range interpolation')
      if np.isinf(errd_test[:,mini]).any():    
        raise ValueError('Out of range interpolation')
      betafP = betafP_test[:,mini].copy()
      betadP = betadP_test[:,mini].copy()
      Nx = Nx[mini]; hx = hy = hx[mini];
      # TODO: when Lx!=Ly 
      Ny = Ly / hy
      if np.round(Ny) != Ny:
        print("Adjusting requested Ly for integer grid size...")
        Ny = np.ceil(Ny).astype(np.int); Ly = hy * Ny
    else:
      betafP = betafP_opt
      betadP = betadP_opt
      Lx = Lxopt; Ly = Lyopt; Nx = Nxopt; Ny = Nyopt
      hx = hopt; hy = hopt;

    # set dimensionless radii 
    for iP in range(0,nP):
      cbeta_m[iP] = pRs[iP] / (wm[iP] * hx) 
      cbeta_d[iP] = pRs[iP] / (wd[iP] * hx) 

    # double the number of points under kernel for reference computation
    if ref:
      hx /= 2; Nx *= 2
      hy /= 2; Ny *= 2

   
    ################# printout kernel settings ############ 
    print('\n\tFinal kernel settings:\n')
    sout = [['R_h','w_m','w_d', 'beta_m','beta_d']]
    for iP in range(0,nP):
      if pSet == pSet_test:
        break;
      if pRs[iP] not in pSet_test:
        pSet_test.add(pRs[iP])
        sout.append([pRs[iP], wm[iP], wd[iP], betafP[iP] * wm[iP], betadP[iP] * wd[iP]])
    for row in sout:
      print("\t{:^16}\t{:^5}\t{:^5}\t{:^16}\t{:^16}".format(*row))
    ##########################################################
    
  return Lx, Ly, hx, hy, Nx.astype(np.int),\
         Ny.astype(np.int), wm, wd, cbeta_m,\
         cbeta_d, betafP, betadP

def configure_grid_and_kernels_z(minZ, maxZ, h, wm, wd, domType, fac=1.5, ref=False):

  """
  Function to set up the z-grid given x-y box length L, z extents, and particle radius Rh
  for several kernel types. This works for triply periodic, doubly periodic no wall, bottom wall and 
  slit channel geometries.

  Input: minZ, maxZ - min/max particle height (or approximate loc of bottom/top bndry in z, both >= 0) 
         domType :
          'TP' - triply periodic
          'DP' - doubly periodic
          'DPBW' - doubly periodic with bottom wall
          'DPSC - doubly periodic slit channel
         fac - safety factor for open boundary placement (see msep*, default=1.5) 
 
  Output:  Lz - length in z (based on domType)
           hz - grid spacing in z to match Rh (hz = 0 if domType != 'TP')
           Nz - number of points in z
           a    - actual floor of z grid [a,b] which we shift to [0,b-a]
                - it is based on the following:
                  msep_down, msep_up - min separation b/w kernel and lower/upper bndry
                    - if domType = 'TP' or 'DPSC', all are 0
                    - if domType = 'DP, msep_down = msep_up = fac*wh/2 
                    - if domType = 'DPBW, msep_down = 0, msep_up = fac*wh/2
                - a = minZ - msep_down 
                - used to shift particle z-coordinates to [0,Lz] from [minZ-msep_down,maxZ+msep_up]
  """
  if domType != 'TP' and domType != 'DP' and domType != 'DPBW' and domType != 'DPSC':
    raise ValueError('domType is not supported')

  hz = h * (domType == 'TP')
  
  # half support with safety factor
  if domType == 'TP' or domType == 'DPSC':
    msep_down = msep_up = 0
  elif domType == 'DPBW':
    msep_down = 0
    msep_up = fac * np.max(wm) * h / 2
  elif domType == 'DP':
    msep_down = msep_up = fac * np.max(wm) * h / 2

  if ref:
    msep_down *= 2.0
    msep_up *= 2.0

  # left/right endpts for z grid and scaling factors for [a,b] -> [-1,1] (if using cheb grid)
  a = minZ - msep_down 
  b = maxZ + msep_up 
  Lz = (b - a);
  # get num points in x,y,z and correct Lx Ly Lz (latter only if domType == 'TP')
  if domType == 'TP':
    Nz = Lz / hz; zpts = zwts = None
    if np.round(Nz) != Nz:
      print("Adjusting requested Lz for integer grid size...")
      Nz = np.ceil(Nz).astype(np.int); Lz = hz * Nz
  else:
    # upsample factor for cheb grid
    H = Lz / 2
    NzFac = 1;
    Nz = np.ceil(NzFac * np.pi/(np.arccos(-h/H) - np.pi/2)).astype(np.int);
    # find nearest larger Nz fft friendly signal length
    Nz = fft_friendly_sizes(Nz, 100, 1); 
    # correction so 2(Nz-1) is fft friendly
    Nz = Nz[0] + 1
  return Lz, hz, Nz.astype(np.int), a 

# helpers for finding fft friendly sizes
def isValid(n):
  while n % 2 == 0:
    n /= 2
  while n % 3 == 0:
    n /= 3
  while n % 5 == 0:
    n /= 5
  while n % 7 == 0:
    n /= 7
  return (n == 1) 

def findBest(N):
  for i in range(0,N):
    if isValid(N + i):
      return N + i
    if isValid(N - i):
      return N - i
  return 0

def fft_friendly_sizes(N, sep, count):
  Ns = []
  c = 0
  for i in range(N, N + sep):
    _N = findBest(i)
    if not _N in Ns:
      if c == count:
        break
      Ns.append(_N)
      c += 1
  return np.array(Ns, dtype = np.int)

